package chapter2;

public class OutputStatements {

	public static void main(String[] args) {
		//Bài 2.32
		System.out.println("1" + 1);
		System.out.println('1' + 1);
		System.out.println("1" + 1 + 1);
		System.out.println("1" + (1 + 1));
		System.out.println('1'  + 1 + 1);
		
		//Bài 2.33
		System.out.println(1 + "Welcome " + 1 + 1);
		System.out.println(1 + "Welcome " + (1 + 1));
		System.out.println(1 + '\u0001' + 1);
		System.out.println(1 + 'a' + 1);
	}

}
