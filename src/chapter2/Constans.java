package chapter2;

public class Constans {
	public static final int SIZE = 20;
	
	public static void main(String[] args) {
		System.out.println("gia tri cua hang so la: " + SIZE);
		
		System.out.println("Cong thuc tinh so mu: " + Math.pow(2, -3));
		
		System.out.println("Min byte " + Byte.MIN_VALUE + " and Max byte " + Byte.MAX_VALUE);
		System.out.println("Min Short " + Short.MIN_VALUE + " and Max Short " + Short.MAX_VALUE);
		System.out.println("Min Integer " + Integer.MIN_VALUE + " and Max Integer " + Integer.MAX_VALUE);
		System.out.println("Min long " + Long.MIN_VALUE + " and Max long " + Long.MAX_VALUE);
		System.out.println("Min float " + Float.MIN_VALUE + " and Max float " + Float.MAX_VALUE);
		System.out.println("Min Double " + Double.MIN_VALUE + " and Max Double " + Double.MAX_VALUE);
		
		
		System.out.println(-34 % (-5));
	}

}
